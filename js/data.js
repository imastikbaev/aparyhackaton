/* ── Local search function ──────────────── */
function searchLocal(query) {
  var q = query.toLowerCase().trim();
  if (!q) return [];
  return UKA_ADDRESSES
    .filter(function(a) {
      return a.address.toLowerCase().includes(q) ||
             (a.info && a.info.toLowerCase().includes(q));
    })
    .slice(0, 8)
    .map(function(a) {
      return {
        address:        a.address,
        additionalInfo: a.info,
        latitude:       a.lat,
        longitude:      a.lng,
        type:           a.type,
        local:          true,
      };
    });
}
